#include "libft.h"

int	is_vowel(char v)
{
	int		i;
	char	*vowels;

	vowels = "aeiou";
	i = 0;
	while (vowels[i])
	{
		if (vowels[i] == v)
			return (1);
		i++;
	}
	return (0);
}
